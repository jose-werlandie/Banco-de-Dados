CREATE DATABASE Escola_de_Musica

USE escola_de_musica;

CREATE TABLE orquestra 
	  (Codigo int, Nome varchar(50), 
	   Cidade varchar(30), 
	   Pais varchar(15), 
	   DataCriacao date, 
	   PRIMARY KEY (Codigo));

CREATE TABLE sinfonia 
	  (Codigo int, 
	   Nome VARCHAR(50), 
	   Compositor VARCHAR(100), 
	   DataCriacao DATE, 
	   PRIMARY KEY(Codigo));

CREATE TABLE musico 
	  (Matricula int, 
	   Nome VARCHAR(50), 
	   Snome VARCHAR(100),  
	   RG int, Nacionalidade VARCHAR (50), 
	   DataNascimento DATE, FuncaoTradicional VARCHAR(50),
       fk_orquestra_codigo int, 
	   PRIMARY KEY(Matricula));

CREATE TABLE instrumentos 
	  (Codigo int, 
	   Nome varchar(30), 
	   Classificacao varchar(20), 
	   PRIMARY KEY(Codigo));

CREATE TABLE apresenta
	  (Codigo int,
	   fk_sinfonia_codigo int, 
	   fk_musico_matricula int, 
	   InstrumentoUtilizado VARCHAR (50), 
	   DataApresentacao DATE, 
	   FuncaoEspecifica VARCHAR(50),
	   PRIMARY KEY (Codigo));

CREATE TABLE executa 
	  (Codigo int,
	   fk_sinfonia_codigo int, 
	   fk_orquestra_codigo int, 
	   DataExecucao DATE, 
	   Desempenho int, 
	   PRIMARY KEY (Codigo));

CREATE TABLE toca (fk_musico_matricula int, fk_instrumento_codigo int );

